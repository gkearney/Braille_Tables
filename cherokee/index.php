<h2>Online Braille Translator</h2>
<p>This online <a href="iubraille.php">Inuktitut syllabary Braille translator</a> can be used to translate limited amounts of Inuktitut syllabary text to a Braille ready file (.brf). It employs the Braille table for liblouis which can be downloaded below.


<h2>Braille Translation Table</h2>
<p>A Braille translation table for the <a href="http://www.liblouis.org">liblouis Braille system</a> has been developed. This table when used with the various liblouis software will transcribe works in the Inuktitut syllabary into ASCII Braille for production on modern computer driven Braille embossers.</p>

