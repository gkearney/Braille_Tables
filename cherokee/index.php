<h2>Abstract</h2>
<p><blockquote>This paper presents a proposal for a Braille code for the Cherokee syllabary. At the time if this writing the Cherokee syllabary does not have a Braille code. The absents of such means that blind speakers of the Cherokee language are unable to learn and read the language in its native form or to have understanding of the Cherokee syllabary writing system. This braille code seeks to address this problem and to provide full literacy to blind speakers of this language.<br/><div class="rl"> &mdash;Tamara Johnson Kearney, Cupertino, California May 2014</div></blockquote></p>

<h2>Introduction</h2>
<p>I have been a long time advocate for literacy, particularly braille literacy. I also feel passionately the importance of preserving languages. I believe that the blind as well as the sighted should be able to read and write in their native language. These principles have been my primary motivation for developing this braille code.</p>

<p>My aim has been to develop a code that follows a logical pattern, and is easy to both learn and reproduce on both modern Braille production systems as well as by traditional Perkins Braille Writers and even on slates and styluses. To that end I have retained the six dot cell of traditional Braille. My fervent hope is that people will learn it, and that literature will be produced in it.</p>

<p>Braille, as a rule, requires much more space to represent the same information than does print. This is the case with this Braille code as well which adds about one-third more space to the length of any written passage. This is expected and is consistent with Braille in other languages.</p>

<h2>History of the Cherokee syllabary</h2>
<p>The Cherokee syllabary is the invented of Sequoyah (ᏎᏉᏯ) [c. 1770-1843] used to read and write the Cherokee language. Developed in the late 1810s and early 1820s. The creation of the syllabary is noteworthy in that Sequoyah could not previously read or write in any script. It remains today the only example of a modern writing system developed independent of a previous background in an existing writing system.</p>

<p>Each symbol represents a syllable rather than a single phoneme; the 86 characters in the Cherokee syllabary provide a suitable method to write Cherokee.</p>

<p>As of 1980, the Cherokee language is still spoken both formally and informally by around 10,000 Cherokees primarily located in the U.S. states of Oklahoma and North Carolina. The language remains strong.</p>

<h2>History of Braille</h2>
<p>Braille is named after its creator, Frenchman Louis Braille, who became blind as a result of a childhood accident. At the age of 15, Braille developed his code for the French alphabet in 1824 as an improvement on night writing used by the French army of the time. He published his system, which subsequently included musical notation, in 1829.</p>

<p>Today Braille is an international standard for tactile reading. Braille codes have been developed for most languages having a written form. Braille literacy and education is important for developing reading skills among blind and visually impaired children. Braille literacy directly correlates with higher employment rates in this population.</p>

<h2>The Braille cell</h2>
<p>Braille characters are small rectangular blocks called cells that contain tiny palpable bumps called raised dots. The number and arrangement of these dots distinguish one character from another. The dots are numbered from 1 to 6 starting at the left column 1,2,3 and followed by the right column 4,5,6. This permits the notation of Braille cells by numbers as used in the tables below.</p>
<p style="font-size: 32pt;" align="center">1 ●● 4<br/>
2 ●● 5<br/> 
3 ●● 6</p>

<p>This Braille code references the Unified English Braille code in use in the English speaking world as a whle for signs and layout usage not unique to the Cherokee syllabary</p>
<h2>The Cherokee syllabary</h2>
<p>The syllabary is set out in  glyphs representing the sound of a vowel or consonant vowel pairings.</p>

